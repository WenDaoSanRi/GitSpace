SELECT
	PRO_CODE,
	CONSIGNMENT_AMT_MIN,
	--代销额度下限
	CONSIGNMENT_AMT_MAX,
	--代销额度上限
	CONSIGNMENT_AMT,
	--已代销额度
	CASE CONSIGNMENT_AREA
WHEN '1' THEN
	'部分机构'
ELSE
	'全部机构'
END AS CONSIGNMENT_AREAName,
 --代销范围
CASE RESERVE_YN
WHEN 'Y' THEN
	'预约'
ELSE
	'不预约'
END AS RESERVE_YNName,
 --是否预约
CASE SMALL_INV_YN
WHEN 'Y' THEN
	'控制小额'
ELSE
	'不控制小额'
END AS SMALL_INV_YNName,
 --是否启用小额投资人控制
SMALL_AMT * 10000,
 --小额合同金额
SMALL_NUM_MAX,
 --小额合同数量
SMALL_NUM --已实现小额合同数量
FROM
	PRO_ITPS
WHERE
	PRO_CODE LIKE 'Z00001_ITP%';


